# Number System Conversion
"""
1. Binary - 0 to 1; base 2
2. Decimal - 0 to 9; base 10
3. Octal - 0 to 7; base 8
4. HexaDecimal - 0 to 9 & A - F; base 16
"""

# Decimal to Binary, Octal, HexaDecimal
numD = 25
print("Binary: ", bin(numD))
print("Octal: ", oct(numD))
print("HexaDecimal: ", hex(numD))

# Binary to Decimal, Octal, HexaDecimal
numB = 0b11101101
print("Decimal: ", int(numB))
print("Octal: ", oct(numB))
print("HexaDecimal: ", hex(numB))
